package com.example.winkylite.models;

public class DataPoint {
    private final float x;
    private final float y;

    public DataPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }
}
